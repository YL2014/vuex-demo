			<p>黄花梨家具木质的辨识困难往往出于三个方面。一是与红木不分。深色黄花梨，要是使用的年头久了，保存状态又不好，乍一看与红木非常像。其实从本质上判定，黄花梨木的木性小，故变形率也小，体轻而温和。把握这点，其它问题就可以迎刃而解；二是与草花梨不分。草花梨的出现是由于黄花梨木材断绝，作为补充而在晚清至民国出现于市场的，在硬木中最为低档。其木质粗疏，棕眼过大，色土黄而无光泽；三是与新黄花梨木不分，新材份量也比老材黄花梨木重，木纹含黑线过多而且生硬，因此许多木纹过于漂亮抢眼的反倒是新黄花梨木。</p>
			<p>了解黄花梨木的特性，对辨识其材质十分重要。比如黄花梨木有很强的韧性，不像红木那样脆。木匠在施工中辨识黄花梨木和红木十分容易，在刨刃口很薄的情况下，只有黄花梨木可以出现类似弹簧外形一样长长的刨花，而红木只出碎如片状的刨屑，有关这点任何一名硬木木匠都很清楚。以上是辨识黄花梨木的初级阶段，更重要的是从造型上判断，黄花梨家具盛行的十七、十八世纪，已将黄花梨家具定了位，造型虽千变万化，文人化的本质却非常统一。从本质上理解黄金时代的中国明式家具，再加上对黄花梨木物理性的了解，辨识黄花梨就会变得十分简单。</p>
