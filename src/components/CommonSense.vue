				<p><span>土壤要求 </span>土壤为褐色砖红壤和赤红壤。</p>
				<p><span>地形地势条件</span>本种对立地条件要求不严，在陡坡、山脊、岩石裸露、干旱瘦瘠地均能适生。为阳性树种，在过分荫蔽的密林中，幼树生势衰弱；在郁闭度较小的林分中能长成直干大树。结实虽然丰富，但天然下种时正值旱季，故林下幼树不多。</p>
				<p><span>人工栽培萌芽力强</span>萌芽力较强，现存林木多为萌生小径木。天然林木生长较慢，人工栽培的林木生长较快，长势良好。</p>

