			<p>地域的限制仅是黄花梨稀缺的原因之一。一棵黄花梨树需要至少300~500年的生长期才能有加工成家具的可能，但并非整棵黄花梨树都可以作成家具原材料，黄花梨树外面的边材部分为淡黄色无味的软质部分，最受白蚁们的欢迎，白蚁会在三年左右的时间将边材咬蚀，当遇到有辛辣方向而又坚硬的心材部分才会被保留了下来，这就是意味着整棵树只有三分之一的部分可用于家具制作。漫长的生长周期和少许可利用的部分无疑更增添了黄花梨资源的稀缺性。事实上，除了资源的稀缺外，海南黄花梨纹理炫美瑰丽、材质油韧细密、触摸起来温润如玉，而且具有降血压和治疗心血管的药用价值，让它成为“木黄金”和“木材大熊猫”。黄花梨家具肌理花纹如行云流水，不用上漆，只需稍稍打蜡就很美；而且样式简洁、线条明快、返璞归真，能给人充分的想象空间。黄花梨家具的美含蓄不张扬，符合审美，也是中国文人追求的境界，也正是如此，海南黄花梨成为红木家具尤其是明清古典家具最受推崇的首选木材。黄花梨古典家具因其存世稀少、做工考究、实用美观等独具优势，成为了藏家瞩目的焦点，其价格更是一路暴涨，贵比黄金。</p>
			<p>随着红木家具市场需求逐年增加，原材料日已枯竭，加上受经济危机影响，红木在2007年前后经历了短暂低迷行情之后，价格只升不跌，保守估计，红木家具的市场价格，未来年平均增幅在25%左右。在目前各类红木家具中，市场价值最高的是海南黄花梨，直径20厘米、长度1~1.5米左右的海南黄花梨材料，市场价格约为3000~3500元人民币/500克。</p>
