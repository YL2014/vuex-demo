			<h2>深厚悠远的海黄历史文化</h2>
			<p>海南黄花梨的历史源远流长，海南黄花梨自古就备受皇室贵族的青睐，被皇家视为珍贵木材。唐朝时，已经是大臣敬献皇家宝物的首选，但是也正是因为皇家的喜爱，黄花梨的资源也随着人们的开采越来越少，知道现在已经被列为国家珍稀保护植物之一。<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;据《博物要览》记载：“花梨产交（即交趾）广（即广东、广西）溪涧，一名花榈树，叶如梨而无实，木色红紫而肌理细腻，可作器具、桌、椅、文房诸器。”《广州志》云：“花榈色紫红，微香，其纹有若鬼面，亦类狸斑，又名和‘花狸’。老者纹拳曲，嫩者纹直，其节花圆晕如钱，大小相错者佳。”《琼州志》云：“花梨木产崖州、昌化、陵水。”明代黄省曾《西洋朝贡典录》载：“花梨木有两种，一为花榈木，乔木，产于我国南方各地。一为海南檀，落叶乔木，产于南海诸地，二者均可作高级家具。”书中还指出，海南檀木质比花榈木更坚细，可为雕刻用。按《古玩指南》第二十九章所说：“花梨为山梨木之总称，凡非皆本之梨木，其木质均极坚硬而色红，惟丝纹极粗。”</p>
