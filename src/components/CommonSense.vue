			<h2>黄花梨作品介绍</h2>
			<p>很长一段时期，对黄花梨家具的认识进入误区，以为明式黄花梨家具大都是明朝生产的。而事实上恰恰相反，黄花梨家具生产的黄金时代是清前期至乾隆这一百多年，嘉庆以后就几乎不再生产。原因是明晚期嘉靖万历两朝，宫廷家具均以漆器为主。华丽的漆制家具占领市场，黄花梨家具显然不是主流。而清式家具由宫廷形成后才进入民间，这需要一定的时间，与人们的想象会有不小差距。因此，不能仅以“明式”、“清式”来断定年代。其实，绝大部分古家具的断代都是由制做手法提炼成“符号”以后综合决定的，了解细微变化，对判定明式家具孰明孰清才颇为重要。</p>
			<div>
				<div>
