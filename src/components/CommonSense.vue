			<p>用檀香木、紫檀、黄花梨等名贵材料制成的传统硬木家具有一定的环保性能，而且还有独特的药理作用。日常用于防虫的樟脑就取自于樟木，用樟木制作的家具自然也有防虫的作用。而紫檀香气比较淡，但好闻，优雅，沁人肺腑，衣服纳于其间，日久生香。另外，酸枝木和香枝木类也都有一些淡淡的清香，弥漫在空气中对人的身心都有益。 当然，对身心最有益的首推海南降香黄檀，俗称黄花梨，亦称“降压木”，原产于我国海南岛罗山尖峰岭低海拔的平原和丘陵地区，《本草纲目》中称为降香，即有降血压、血脂及舒筋活血的作用。海南降香黄檀入药一般情况下是用其木屑，如用木屑泡水，可以降血压、血脂；用木屑填充做枕头更有舒筋活血之功效，尤其适合于老年人使用。用海南降香黄檀制成的家具，如床榻和椅凳之类，对睡眠和养神是最为有益的。</p>
