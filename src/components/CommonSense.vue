			<h2>堪比黄金的市场价格</h2>
			<p>一千克的木头就可以兑换40克的黄金？听起来这是一件很不靠谱的事情，但这的确就发生在我们身边。2007年，北京某红木家具公司推出一项“黄金换木头”的活动，主办方称不管是擀面杖还是瘸腿凳子，只要是海南黄花梨做的，一千克黄花梨木就可以兑换40克的黄金。虽然最终一克黄金也没能兑换出去，但这却让当年海南黄花梨一下子名声大噪，更多的人将目光锁定了海南黄花梨。</p>
			<p>时隔三年，作为内地拍卖公司的领军者之一，中国嘉德“明式黄花梨家具精品展”如期而至，60余件明清时期的海南黄花梨木家具集体亮相，这其中包括从国外漂洋过海而来的众多名贵古典家具精品，海南黄花梨再次成为收藏界的焦点。</p>
			<p>十年间，国际黄金平均价格从200美元/盎司左右暴涨到当前的1300美元/盎司左右，疯狂飙升5倍多。而比黄金更加疯狂的却是有“木黄金”之称的海南黄花梨。这些年海南黄花梨价格暴涨，收购价从1979年前后的每500克4.5元，到1992年前后涨到每500克6元，2002年均涨到每500克10元，现在攀升到每500克4000~5000元的高价。最近的八年，海南黄花梨价格翻了400多倍。</p>
			<p>据2010年上半年国内部分著名木材均价表，与其他几种名贵木材相比，海南黄花梨的价格无疑让人咋舌。以海南黄花梨木做成的三件套皇宫椅为例，2000年左右时，市场价大概为45000~60000元，而现在市场价格达到130万~180万元，翻了近30倍。与原木上涨400倍相比，虽然30倍的上涨幅度并不高，但这已经超过了很多人所能接受的范围。</p>
