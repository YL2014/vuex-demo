				<p><span>要分布区域</span>主要分布于越南，缅甸，及中国海南、广东、广西、福建等地。</p>
				<p><span>降香黄檀在海南的分布情况</span>降香黄檀在海南零星分布于岛西部、西南部和南部的东方、昌江、乐东、白沙及崖县等地的山林中，可四季采收“降香”。多年来我国林业部门在研究“降香黄檀”的育苗工作中曾投入了大量的人力、物力、财力，虽得以成功但成活率较低，所以目前尚未大面积推广。据专家介绍，“降香黄檀”因地理分布的特点及特有的生长环境对其心材（俗称“格”）的颜色、纹理、花纹、光泽、油性的影响十分明显。岛西部颜色较深、比重大而油性强的心材（油格）价格略高于岛东部色较浅、油性稍差的心材（糠格），而东部地区的“降香黄檀”由于明清时期过度采伐也已近绝迹。</p>

				<h4>主要价值</h4>
				<p><span>木性极为稳定</span>黄花梨木的木性极为稳定，不管寒暑都不变形、不开裂、不弯曲，有一定的韧性，适合作各种异形家具，如三弯腿，其弯曲度很大，惟黄花梨木才能制作，其他木材较难胜任。黄花梨的主要产地是海南岛，自然资源已近枯竭，已无天然林，如今海南、两广甚至福建，均有不少人工林，但是黄花梨虽然生长速度快，但是能用于家居器具制作使用的心材的成长相当缓慢，十年内的树几乎看不到有心材，随着树木的生长，心材慢慢变大，要到能做器具的树，基本上要4、50年以上。</p>
				<p><span>制作家具的用途</span>黄花梨老式家具，黄花梨实为呈黄褐色的"花梨"或"花榈"。是明清硬木家具的主要用材，以心材呈黄褐色为好。明，清时期考究的木器家具都选"黄花梨"制造，其纹理或 隐或现，色泽不静不喧，被视作上乘佳品，备受明清匠人宠爱，特别是明清盛世的文人、仕大夫之族对家具的审美情趣更使得这一时期的黄花梨家具卓而不群，无论 从艺术审美、还是人工学的角度来看都赞不绝口，可称为世界家具艺术中的珍品。 黄花梨木家具制作产地以苏州为代表，做工规范，精工细做，是典型的苏式家具。另外南方两广及海南岛等地明清时期也生产黄花梨木家具，称之为广作或广式家具，其做工略粗，不及苏式家具精细。</p>

				<h4>主要价值</h4>
				<p>由于海黄的价格奇高，在市面上不免出现一些以假乱真、狐假虎威的仿品，那么如何去区分真假海南黄花梨家具呢。</p>
				<p><span>黄花梨的香味</span>海南黄花梨都具有降香黄檀这个树种独特的香味。然而，大家能接触到的海黄香味却有许多的“变味”，有的甚至是臭味。这就要求大家走出光靠“味道辨别”海黄的误区。 海黄的香味并不像沉香那样浓密而且香味持久，只有新切面或者封严的杯子、罐子才好闻到；一旦新切面暴露在空气中，不久香味就慢慢淡去，直到刮开新的表面就又会散发出来。因此，不要以为你的宝贝闻不到香味了，就开始怀疑它不是真正的海黄。 此外，即便在雨天或者潮湿的天气，海黄也会散发出悠悠降香。当然，也有千年难遇的老料，这样的老料做出来的宝贝，哪怕上蜡也难以封住它的香味，而且非常醇香。</p>
				<p><span>黄花梨的花纹</span>海黄除了香味能让人上瘾，就是花纹也能令人陶醉。海黄的花纹有千奇百怪的“鬼脸”，有密密麻麻的“鬼眼”，也有像“虎皮”一样的纹路。新玩家往往凭这些概念来判断海黄的真假与好坏。其实，海黄的花纹、纹路、线条有许多种形式，不是所有的海黄都有“鬼脸”和“鬼眼”。 海黄的花纹有粗有细，但都很清晰，不显乱，有流线的，有弯曲的，甚至有直的；以黑线花纹居多，偶尔也能见到深褐色或红线花纹，多为海南东部黄梨；细花纹的材质颜色往往色差不大，粗花纹的花纹颜色跟材质颜色色差明显，整体颜色又非常好看，多为海南西部油梨。 这里要提醒大家的是，并不是所有的海黄都有漂亮的花纹，许多老料大料，老料的边脚料做出来的东西就没有什么花纹，却又很值钱。</p>
				<p><span>黄花梨的材质</span>谈黄花梨就不得不说到它的材质，以材质花纹来区分海南黄花梨本身就有无数种，没有任何两件相同的器物是相同材质花纹的。材质也是海黄跟越黄较大的区别，大家所能接触到的绝大部分的海南黄花梨材质表面都能比较容易打磨出荧光，扫腊后什么荧光感，半透明琥珀质感，温润如玉感都出来了，而少数长期泡水的海黄根料或杆料会出现像越南黄花梨一样的材质表面，光打磨不上腊反而要好看的多。海黄打磨后，表面毛孔较少，甚至没有，反光感很强;越黄就毛孔较粗，看起来材质有些疏松，感觉吸光，上腊后二者对比就更加明显。</p>
				<p>海南黄花梨气干密度为0.82-0.94/cm³，仅有少数重的料或成品入水即沉，多数都会有点浮出水面，所以，不沉水就不是好料这样理解也是错误的。黄花梨又分为东部的黄梨(颜色发黄)和西部的油梨(红、紫、黑三种颜色都有)，东部的黄梨相对西部的油梨来说，材质要轻，做为古时进贡皇家的木料，最为出名的形容就是荧光感强，半透明质感透露出屡屡金丝。</p>

