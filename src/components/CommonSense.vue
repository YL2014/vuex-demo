				<p><span>手感</span>海黄表面细腻，有光滑，温润如玉的手感，而越南黄花梨相比之下则比较粗糙。出现这种现象主要是因为海黄有极强的密度、极细的纹理细胞组织和非常密实的材质结构。另外一个重要的原因是海黄的含油( 降香油)的比例。经相关检测发现，海黄木最高含油量达到27%，而越黄木则只有0.5%。</p>
				<p><span>鬼脸</span>人们把海黄的特征归之为有“鬼脸”。事实上，所谓“鬼脸”就是树木生长过程中的结节、疙瘩。海黄由于生长期长，生长环境恶劣，而且枝节较多，树木纹理多为扭曲、交错无规律，故“鬼脸”“鬼眼”等图纹更多，更丰富。相对而言，越黄生长期短，纹理大多数是平顺自然，所以越黄的鬼脸图纹则单调。</p>

