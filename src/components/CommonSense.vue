			<p>“全世界很少能有一个民族做家具用这么硬的木材，在古代没有机械加工，因此这是非常困难的一件事情，只有中国人可以做，中国人很愿意为难自己，一个是维持生活，一个是享受这个乐趣，中国历史上都是拿软笔写字，不是中国人不知道硬笔写字快，主要是修身养性，家具也是这样，在几百年前，工具都没有这么好，包括施工的东西没有达到现在钢的硬度，做这样的家具投入非常大的。这是黄花梨家具从根本上最吸引人的地方。”</p>
			<p>“黄花梨那个土财主味特少，而且它是文化品位的象征。”</p>
			<p>“黄花梨本身有两个创造，第一个是自然而生，这个木头长成什么样和你想的没有关系，   另外一个就是人工的，因为他们都是能工巧匠，他们又包含了另一层文化内涵，这两个进去，一个是自然属性，当然了，所有的自然属性都是文人赋予他的。”</p>
			<p>“大部分的人希望我买了这个家具之后升值，能够有利益上的回报，这个利益的回报很多是精神层次的。当时我买这个东西50万，现在值80万了，这是心理的一个愉悦，他可能从经济目的进入这个领域，但是最终会获得很多文化的乐趣。收藏呢，即便他是从逐利开始，最终还是归结到文化上，因为黄花梨家具存在了几百年了，有很多的文化信息。”</p>
			<p>“最早黄花梨家具是属于中国的贵族家具，是文人参与设计的，这些人参与设计是跟一般的工匠直接设计是不一样的，文化内涵特别多，即便是素的黄花梨家具，也汇集了大量的文化符号。有时候你感觉什么也没有，但是很奇怪，四百年之后今天的西方人认为很厉害，设计的这么现代，不敢想象是古代人设计的，这也是黄花梨家具受全世界公认的因素。”</p>
			<p>“黄花梨家具现在已经成为中国的文化符号之一，它囊括的文化内涵不仅体现在家具作品的造型上，更因其几百年来的设计。现在没人敢用这么贵重的材料冒风险，大家都没有信心去超过古人，因为这个文化符号太深重了。”</p>
			<p>“中国人喜欢在文化上做文章，并将这些文化符号印刻在生活起居上。就比如说这世上没有一棵叫紫檀这个名字的树，黄花梨家具没有叫黄花梨的树，包括红木也没有一种树叫红树，它是带有很强烈的色彩，带有文化的东西，因为中国人认为黄色、红色、紫色都是很吉祥的东西，所以起名的时候都是逐渐演化的。”</p>
			<p>“中国传统家具里面精华的东西是作为艺术品出现的，而不是实用品出现的。不仅仅是一个使用的家具，重要的是还原古代文化的艺术品，摆在你家又收藏了，又实用了。我偶尔也逛家具市场，发现今天的家具都或多或少的表明一些中国的文化色彩，表明中国文化的力量。”</p>
			<p>“近年来大家重新喜欢上了中国的传统文化，包括家具、装饰等等。人们在选择中式传统家具时是想更深入的接近中国历史文化。这股古典家具风不仅导致更多的海外投资者进入中国寻找具有收藏价值的货物，在国内，也使一些仿古家具备受推崇。”</p>

			<h4>海岩和黄花梨家具结缘</h4>
			<p class="intro">海岩，原名侣海岩，1954年11月7日生于中国北京，中国作家，编剧，主要作品有：《拿什么拯救你，我的爱人》《玉观音》</p>
			<div>
				<img src="/static/img/commonsense/haiyan.jpg">
			</div>
			<p>一提起黄花梨，海岩总是有说不完的话。海岩的作家底蕴让他对黄花梨的表达也很浪漫，如同他写小说、做酒店一样，他对黄花梨可是情有独钟，玩起黄花梨也是有声有色，在古典家具收藏圈子里也能独树一帜。</p>
			<p>据海岩介绍，他与黄花梨家具结缘应该是在十年前，他说：“有个小故事是起缘。那时和几个朋友路过北京燕莎的一家明清家具店，我想下车去看看，朋友们说这个不能看，那里卖的是黄花梨家具，看了会中毒，就拔不出来了。听他们这么说，我越发要下车一看究竟，结果，果不其然，这一看，中毒到现在还没有消退。在此之前，其实我比较喜欢古罗马传统风格的家具，看了明式风格的家具后就不喜欢了，明式家具中的那种简淡清雅、不事张扬的绚美，是打动我的原因，而且随着年龄的增长，个人的审美口味又慢慢地向自己的本土文化靠拢，这就是根。我们自小接受的文化和生活环境，对中国古代文化潜移默化的感受和了解，时刻在血液里流动潜藏，总有一天会涌动扩展，寻求外在的融合，传统家具就是我的这个融汇点 ”。</p>
			<p>为什么海岩会对黄花梨如此钟爱呢？从木材本身看，黄花梨的木性非常温和，不容易干裂、变形，它虽然硬度很高，但又有一定的柔韧性，能打磨得像一块玉一样，还有就是黄花梨的花纹变化多姿，各种自然现象可以融化其中，如同形容人的千人千面，黄花梨也是千目千面。黄花梨的肌理细腻程度，在其他木材中很难找到能与之相比的，不用上漆、着色，只需要用砂纸和蜡稍加打磨照样很美观。它的光滑温润，代表了中国文化的一种追求自然的境界，那种美感，那种天然合一，会带给人无穷想象。几百年来一直很受人们欢迎，尤其是一些文人、艺术家、收藏家的青睐对象。</p>

			<h4>杨波</h4>
			<p class="intro">杨波，男，1964年1月生于中国河北，祖籍山西，奢侈品，艺术品“元亨利”古典硬木家具创始人，黄花梨教父。中国优秀企业家，国际大收藏家联合会常务副会长</p>
			<div>
				<img src="/static/img/commonsense/yangbo.jpg">
			</div>
			<h5>杨波：亲历与见证黄花梨辉煌</h5>
			<p>“感恩花梨情，醉美三月三”。日前，由海南省人民政府主办，海南省文化厅、省旅游发展委员会、东方市人民政府承办的第二届海南黄花梨文化节，在海南省东方市可容纳数万人的“三月三”文化生态公园隆重举行，规模宏大，盛况空前。海南省委、省政府和东方市委市政府领导出席了盛大的开幕式，著名主持人毕福剑主持文艺晚会。中国家具协会传统家具专业委员会主席团轮执主席、北京元亨利硬木家具有限公司董事长杨波作为中国古典家具产业代表人物和黄花梨文化的重要推动者，应当地政府邀请出席了这次活动，并在开幕式上发表讲话。</p>
			<p>海南黄花梨，一种中国特有的树木，短短十几年间，写下了一连串精彩而又传奇的故事；其间围绕黄花梨发生的一切，已越过文化或经济范畴，在更为宽泛的时代背景下诱发一连串绚烂夺目的社会文明事件。而今，提起这一切，似乎谁都无法回避一个人的名字——那便是先后被媒体称作“拆房专业户”、“黄金换木头”始作俑者以及“黄花梨教父”的杨波。</p>
			<h5 class="h5-last">以十数年之功，再现数百年辉煌</h5>
			<p>如果说，中国古典家具艺术在明清两代达到了一个高峰，那么，引领这一家具艺术高峰的材质和树种首推黄花梨。明清两代，海南黄花梨以行云流水的纹理、圆润剔透的质感，引发了文人士大夫的狂热追逐，并最终征服了紫禁城，成就了明清家具的辉煌。自明代起至清前期，黄花梨家具接连问世，以其纹理秀美、格调古雅、简洁明快、富丽堂皇，色泽华丽，典雅尊贵……在中国历史上写下了华丽的章节。</p>
			<p>自清中期起，随着黄花梨可用之料日益减少和日渐稀缺，来自东南亚的紫檀、红酸枝（时称海梅木）等木材才渐成古典家具用材主角。然而，精美绝伦的黄花梨并没退出历史舞台，16世纪末至17世纪初，有大量硬木家具从宫廷、王府流出。当时在华的西方传教士看到典雅精美的中国古典家具后，惊为天工，纷纷购买运回欧洲。鸦片战争前后，又有大量黄花梨家具被欧洲商人购走。</p>
			<p>在国门敞开西风东渐的同时，中国的家具文化也反过来影响到了西方。十八世纪中叶，享誉欧洲的家具设计大师齐彭代尔曾以明式黄花梨家具式样为英国皇室设计了一套宫廷家具，轰动了整个欧洲。自此，许多西方设计师纷纷从中国古典家具中寻找创新灵感。</p>
			<p>黄花梨也曾令上世纪初来华任教的德国教授古斯塔夫?艾克长久痴迷，艾克研究认为，发端于明代的黄花梨家具“不论在技术加工、选材用料还是设计理念方面，都已达到了最高峰”。为此，他把明代家具作为科研对象，将其结构进行模拟，解剖家具榫卯，拍照测绘，倾心研究十四个年头，于1944年在北京出版了《中国花梨家具图考》，全书共收录122件家具实物，30余张精确的测绘图纸，4张解析清楚的中国家具榫卯构造图纸。当时每本售价五十块银元，一时洛阳纸贵！在稍后一段历史中，因为时代原因，艾克等人对黄花梨的研究一度成为绝响。</p>
			<p>直到上世纪八十年代，王世襄《明式家具珍赏》《明式家具研究》等书问世，书中的绝对“主角”黄花梨终于在世人全新的目光关照下重新放出异彩。1991年，中国明式家具学会名誉主席、中国古典家具学者陈增弼先生与多位同好戮力推出《中国花梨家具图考》中文版，此书和王世襄的著作一道，一时成为中国古典家具从业者、研究者和收藏鉴赏人士的“圣经”。而此时，世上的黄花梨家具已所见无几。</p>
			<p>一切迹象表明：时代在向历史深处呼唤黄花梨，呼唤黄花梨家具迷人的身影，呼唤黄花梨文化的再度兴起。</p>
			<p>似乎是偶然，也似乎是一种天意与必然——杨波的出现，于不经意间被历史选中，由此，开始了“再造黄花梨辉煌”的一段历史；他得以用十数年之功，再现黄花梨数百年前的辉煌……</p>
